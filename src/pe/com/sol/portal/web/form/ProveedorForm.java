
package pe.com.sol.portal.web.form;

/**
 * @author Alonso Gutierrez
 * 
 */
public class ProveedorForm {
	
	private String id;
	
	private String ruc;
	
	private String razonSocial;
	
	private String descripcion;
	
	private String direccion;
	
	private String pais;
	
	private String fonoMovil;
	
	private String fonoFijo;
	
	private String correo;
	
	private String tipoPersona;
	
	private String tipoBusqueda;
	
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the ruc
	 */
	public String getRuc() {
		return ruc;
	}

	/**
	 * @param ruc the ruc to set
	 */
	public void setRuc(String ruc) {
		this.ruc = ruc;
	}

	/**
	 * @return the razonSocial
	 */
	public String getRazonSocial() {
		return razonSocial;
	}

	/**
	 * @param razonSocial the razonSocial to set
	 */
	public void setRazonSocial(String razonSocial) {
		this.razonSocial = razonSocial;
	}

	/**
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}

	/**
	 * @param descripcion the descripcion to set
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	/**
	 * @return the direccion
	 */
	public String getDireccion() {
		return direccion;
	}

	/**
	 * @param direccion the direccion to set
	 */
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}

	/**
	 * @return the pais
	 */
	public String getPais() {
		return pais;
	}

	/**
	 * @param pais the pais to set
	 */
	public void setPais(String pais) {
		this.pais = pais;
	}

	/**
	 * @return the fonoMovil
	 */
	public String getFonoMovil() {
		return fonoMovil;
	}

	/**
	 * @param fonoMovil the fonoMovil to set
	 */
	public void setFonoMovil(String fonoMovil) {
		this.fonoMovil = fonoMovil;
	}

	/**
	 * @return the fonoFijo
	 */
	public String getFonoFijo() {
		return fonoFijo;
	}

	/**
	 * @param fonoFijo the fonoFijo to set
	 */
	public void setFonoFijo(String fonoFijo) {
		this.fonoFijo = fonoFijo;
	}

	/**
	 * @return the correo
	 */
	public String getCorreo() {
		return correo;
	}

	/**
	 * @param correo the correo to set
	 */
	public void setCorreo(String correo) {
		this.correo = correo;
	}

	/**
	 * @return the tipoPersona
	 */
	public String getTipoPersona() {
		return tipoPersona;
	}

	/**
	 * @param tipoPersona the tipoPersona to set
	 */
	public void setTipoPersona(String tipoPersona) {
		this.tipoPersona = tipoPersona;
	}

	/**
	 * @return the tipoBusqueda
	 */
	public String getTipoBusqueda() {
		return tipoBusqueda;
	}

	/**
	 * @param tipoBusqueda the tipoBusqueda to set
	 */
	public void setTipoBusqueda(String tipoBusqueda) {
		this.tipoBusqueda = tipoBusqueda;
	}
	
}
